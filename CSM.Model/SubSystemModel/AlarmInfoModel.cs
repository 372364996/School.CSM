using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CSM.Model.SubSystemModel
{
   public  class AlarmInfoModel
    {
        /// <summary>
        ///此字段获取报警缓存
        ///0未取
        ///1已取
        /// </summary>
        public int alarm_cache_status { get; set; }
        /// <summary>
        /// 设备code
        /// </summary>
        public string code { get; set; }
        /// <summary>
        /// 报警事件
        /// </summary>
        public string  alarm_event { get; set; }
        /// <summary>
        /// 报警等级
        /// </summary>
        public int alarm_level { get; set; }
        /// <summary>
        /// 报警位置
        /// </summary>
        public string alarm_location { get; set; }
        /// <summary>
        /// 报警名称
        /// </summary>
        public string alarm_name { get; set; }
        /// <summary>
        /// 子系统编号
        /// </summary>
        public int alarm_subsystem { get; set; }

        /// <summary>
        /// 报警消息
        /// </summary>
        public string alarm_text { get; set; }
        /// <summary>
        /// 报警时间
        /// </summary>

        public DateTime alarm_time { get; set; }
        /// <summary>
        /// 报警类型
        /// 设备报警：1，设备告警：2
        /// </summary>
        public int alarm_type { get; set; }
    }
}
